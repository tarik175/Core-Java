package day09forloopwhileloop;

public class ForLoopPractices {
    public static void main(String[] args) {
        for(int i=10;i<58;i++){
            System.out.print(i+" ");
        }
        System.out.println("");
        System.out.println("======================");

        for (int i=100;i>43;i--){
            if(i%2==0) {
                System.out.print(i + " ");
            }
        }
        System.out.println("");
        System.out.println("======================");


        for (int i=100;i>43;i--) {
            if (i % 2 != 0) {
                System.out.print(i + " ");
            }
        }








    }
}
